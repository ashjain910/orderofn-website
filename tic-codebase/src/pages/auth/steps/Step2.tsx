type StepProps = {
  nextStep: () => void;
  prevStep: () => void;
  formData: any;
  setFormData: (data: any) => void;
};

import { useState } from "react";

const Step2 = ({ nextStep, prevStep, formData, setFormData }: StepProps) => {
  const [touched, setTouched] = useState<any>({});
  const handleBlur = (field: string) => {
    setTouched((prev: any) => ({ ...prev, [field]: true }));
  };
  const handleNextStep = () => {
    setTouched({
      first_name: true,
      last_name: true,
      gender: true,
      nationality: true,
    });
    nextStep();
  };
  return (
    <div className="step-left">
      <div
        style={{
          position: "relative",
          width: "100%",
          marginBottom: 0,
        }}
      >
        <div className="progress-title">
          {" "}
          <span className="stepNumber">2</span> About you
        </div>
        <div className="progress">
          <div
            className="progress-bar"
            role="progressbar"
            style={{ width: "40%" }}
          />
        </div>

        <span className="step-count">Step 2 of 5</span>
        <div className="step-card">
          {/* FIRST NAME */}
          <label className="form-label">
            First name{" "}
            <span
              style={{ color: "red", fontWeight: "bold", fontSize: "16px" }}
            >
              *
            </span>
            {touched.first_name && !formData.first_name && (
              <span style={{ color: "red", marginLeft: 8, fontSize: 13 }}>
                First name is required
              </span>
            )}
          </label>
          <input
            type="text"
            className="form-control"
            placeholder="First name"
            value={formData.first_name}
            onChange={(e) =>
              setFormData({ ...formData, first_name: e.target.value })
            }
            onBlur={() => handleBlur("first_name")}
          />

          {/* LAST NAME */}
          <label className="form-label">
            Last name{" "}
            <span
              style={{ color: "red", fontWeight: "bold", fontSize: "16px" }}
            >
              *
            </span>
            {touched.last_name && !formData.last_name && (
              <span style={{ color: "red", marginLeft: 8, fontSize: 13 }}>
                Last name is required
              </span>
            )}
          </label>
          <input
            type="text"
            className="form-control"
            placeholder="Last name"
            value={formData.last_name}
            onChange={(e) =>
              setFormData({ ...formData, last_name: e.target.value })
            }
            onBlur={() => handleBlur("last_name")}
          />

          {/* GENDER */}
          <label className="form-label">
            Gender{" "}
            <span
              style={{ color: "red", fontWeight: "bold", fontSize: "16px" }}
            >
              *
            </span>
            {touched.gender && !formData.gender && (
              <span style={{ color: "red", marginLeft: 8, fontSize: 13 }}>
                Gender is required
              </span>
            )}
          </label>
          <div className="d-flex  flex-wrap gap-3">
            <div className="form-check">
              <input
                className="form-check-input"
                type="radio"
                name="gender"
                id="genderMale"
                value="Male"
                checked={formData.gender === "Male"}
                onChange={(e) =>
                  setFormData({ ...formData, gender: e.target.value })
                }
                onBlur={() => handleBlur("gender")}
              />
              <label className="form-check-label  ml-2" htmlFor="genderMale">
                Male
              </label>
            </div>
            <div className="form-check s">
              <input
                className="form-check-input"
                type="radio"
                name="gender"
                id="genderFemale"
                value="Female"
                checked={formData.gender === "Female"}
                onChange={(e) =>
                  setFormData({ ...formData, gender: e.target.value })
                }
                onBlur={() => handleBlur("gender")}
              />
              <label className="form-check-label  ml-2" htmlFor="genderFemale">
                Female
              </label>
            </div>
            <div className="form-check">
              <input
                className="form-check-input"
                type="radio"
                name="gender"
                id="genderOthers"
                value="Others"
                checked={formData.gender === "Others"}
                onChange={(e) =>
                  setFormData({ ...formData, gender: e.target.value })
                }
                onBlur={() => handleBlur("gender")}
              />
              <label className="form-check-label  ml-2" htmlFor="genderOthers">
                Others
              </label>
            </div>
          </div>

          {/* NATIONALITY SELECT DROPDOWN */}
          <label className="form-label">
            Nationality
            <span
              style={{ color: "red", fontWeight: "bold", fontSize: "16px" }}
            >
              *
            </span>
            {touched.nationality && !formData.nationality && (
              <span style={{ color: "red", marginLeft: 8, fontSize: 13 }}>
                Nationality is required
              </span>
            )}
          </label>
          <select
            className="form-control"
            value={formData.nationality || ""}
            onChange={(e) =>
              setFormData({ ...formData, nationality: e.target.value })
            }
            onBlur={() => handleBlur("nationality")}
          >
            <option value="">Please select</option>
            {countries.map((country) => (
              <option key={country.code} value={country.name}>
                {country.name}
              </option>
            ))}
          </select>

          {/* SECOND NATIONALITY SELECT DROPDOWN */}
          <label className="form-label">Second nationality</label>
          <select
            className="form-control"
            value={formData.second_nationality || ""}
            onChange={(e) =>
              setFormData({ ...formData, second_nationality: e.target.value })
            }
          >
            <option value="">Please select</option>
            {countries.map((country) => (
              <option key={country.code} value={country.name}>
                {country.name}
              </option>
            ))}
          </select>

          {/* CV UPLOAD */}
          <label className="form-label">Resume Upload</label>
          <div style={{ position: "relative", width: "100%" }}>
            <input
              id="cv-upload-input"
              type="file"
              style={{
                position: "absolute",
                left: "-9999px",
                width: "1px",
                height: "1px",
                overflow: "hidden",
                opacity: 0,
                zIndex: -1,
              }}
              onChange={(e) => {
                setFormData({
                  ...formData,
                  cv_file: e.target.files?.[0] || null,
                });
                if (e.target) e.target.value = "";
              }}
              tabIndex={-1}
            />
            <label
              htmlFor="cv-upload-input"
              style={{
                display: "block",
                width: "100%",
                background: "#f8f9fa",
                border: "1px solid #ced4da",
                borderRadius: 10,
                padding: "8px 12px",
                color: "#6c757d",
                fontSize: 14,
                height: 40,
                lineHeight: "22px",
                textAlign: "left",
                cursor: "pointer",
                zIndex: 1,
                position: "relative",
              }}
            >
              Click here to select your CV/resume.
            </label>
          </div>
          {formData.cv_file && (
            <div className="mt-2 d-flex align-items-center gap-2">
              <span className="text-success" style={{ fontSize: 11 }}>
                {formData.cv_file.name || "File selected"}
              </span>
              {/* <button
                type="button"
                className="btn btn-sm btn-outline-danger ms-2"
                onClick={() => setFormData({ ...formData, cv_file: null })}
              >
                
              </button> */}
            </div>
          )}

          {/* WHERE DID YOU HEAR ABOUT US */}
          <label className="form-label">Where did you hear about us?</label>
          <select
            className="form-control"
            value={formData.hear_from}
            onChange={(e) =>
              setFormData({ ...formData, hear_from: e.target.value })
            }
          >
            <option value="">Please select</option>
            <option>LinkedIn</option>
            <option>Google Search</option>
            <option>Referral</option>
            <option>Social media</option>
          </select>

          {/* BUTTONS */}
          <div className="d-flex  flex-wrap justify-content-between mt-3">
            <button onClick={prevStep} className="btn btn-secondary px-4">
              Previous
            </button>

            <button onClick={handleNextStep} className="btn btn-primary px-4">
              Next
            </button>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Step2;
